fun main() {
//    var can be reassigned.
    var a =10
    var b="Adarsh Yadav"
    println(a)
    a=120
    println("a after reassigning : "+a)
    println(b)
    b="b after reassigning value"
    println(b)

    val c=10
    val d="Hello"
    println(c)
    println(d)
//    val can not be reassigned.
//    c=14
}